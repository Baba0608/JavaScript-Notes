// variables

// We can define variables using var, let, const.

var a = 10; // stores 10 in variable 10.

let b = 20; // stores integer 20 in variable b.

const c = true; // stores boolean true in variable in c.

// Most used operators
/**
 *  +  => addition
 *  -  => subtraction
 *  *  => multiplication
 *  /  => division
 *  %  => to get remainder
 *
 *  == will check if the value is same or not?
 *  === will check if the value and datatype both are same or not?
 *  && => and
 *  || => or
 *  ^  => XOR
 */

